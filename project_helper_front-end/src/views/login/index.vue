<script>
import { validUsername } from '@/utils/validate'
// import SocialSign from './components/SocialSignin'
const ORIGINAL_THEME = '#409EFF'
const version = require('element-ui/package.json').version // element-ui version from node_modules

export default {
  name: 'Login',
  // components: { SocialSign },
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!validUsername(value)) {
        callback(new Error('Please enter the correct user name'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '11813009',
        password: '123456'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      showDialog: false,
      redirect: undefined,
      otherQuery: {}
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    },
    async theme(val) {
      const oldVal = this.chalk ? this.theme : ORIGINAL_THEME
      if (typeof val !== 'string') return
      const themeCluster = this.getThemeCluster(val.replace('#', ''))
      const originalCluster = this.getThemeCluster(oldVal.replace('#', ''))
      console.log(themeCluster, originalCluster)
      console.log(themeCluster[0])
      console.log('ceshi2')
      let test
      if (localStorage.getItem('saved_theme') != 'null') {
        test = localStorage.getItem('saved_theme')
      }

      var tmp = parseInt(themeCluster[0], 16)
      // if(tmp>4473924){
      //   tmp-=4473924
      // }else
      var minus = true
      for (var i = 0; i < 6; i++) {
        if (themeCluster[0][i] === '0') {
          minus = false
          break
        }
      }
      if (minus) {
        if (tmp > 1118481) {
          tmp -= 1118481
        }
      }
      var outcome = tmp.toString(16)
      while (outcome.length < 6) {
        outcome = '0' + outcome
      }

      var add = true
      for (var j = 0; j < 6; j++) {
        if (themeCluster[0][j] === 'f') {
          add = false
          break
        }
      }
      if (add) {
        if (tmp < 14540253) {
          tmp += 1118481
        }
      }
      var hover = tmp.toString(16)

      console.log(outcome)
      if (localStorage.getItem('saved_theme') != 'null') {
        document.getElementsByTagName('body')[0].style.setProperty('--testColor', '#' + test)
      } else {
        document.getElementsByTagName('body')[0].style.setProperty('--testColor', '#' + themeCluster[0])
      }
      document.getElementsByTagName('body')[0].style.setProperty('--sidebar', '#' + outcome)
      document.getElementsByTagName('body')[0].style.setProperty('--subMenuBg', '#' + outcome)
      document.getElementsByTagName('body')[0].style.setProperty('--menuHover', '#' + hover)
      document.getElementsByTagName('body')[0].style.setProperty('--subMenuHover', '#' + hover)
      // console.log(document.styleSheets)
      // return

      const $message = this.$message({
        message: '  Compiling the theme',
        customClass: 'theme-message',
        type: 'success',
        duration: 0,
        iconClass: 'el-icon-loading'
      })

      const getHandler = (variable, id) => {
        return () => {
          const originalCluster = this.getThemeCluster(ORIGINAL_THEME.replace('#', ''))
          const newStyle = this.updateStyle(this[variable], originalCluster, themeCluster)

          let styleTag = document.getElementById(id)
          if (!styleTag) {
            styleTag = document.createElement('style')
            styleTag.setAttribute('id', id)
            document.head.appendChild(styleTag)
          }
          styleTag.innerText = newStyle
        }
      }
      if (!this.chalk) {
        const url = `https://unpkg.com/element-ui@${version}/lib/theme-chalk/index.css`
        await this.getCSSString(url, 'chalk')
      }

      const chalkHandler = getHandler('chalk', 'chalk-style')

      chalkHandler()

      const styles = [].slice.call(document.querySelectorAll('style'))
        .filter(style => {
          const text = style.innerText
          return new RegExp(oldVal, 'i').test(text) && !/Chalk Variables/.test(text)
        })
      styles.forEach(style => {
        const { innerText } = style
        if (typeof innerText !== 'string') return
        style.innerText = this.updateStyle(innerText, originalCluster, themeCluster)
      })

      this.$emit('change', val)

      $message.close()
    }
  },
  created() {
    // window.addEventListener('storage', this.afterQRScan)
  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {
    // window.removeEventListener('storage', this.afterQRScan)
  },
  methods: {
    checkCapslock(e) {
      const { key } = e
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z')
    },
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          if (localStorage.getItem('saved_theme') != 'null') {
            const theme = localStorage.getItem('saved_theme')
            this.$store.dispatch('settings/changeSetting', {
              key: 'theme',
              value: theme
            })
          }
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm)
            .then(() => {
              this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
              this.loading = false
            })
            .catch(() => {
              this.loading = false
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
    // afterQRScan() {

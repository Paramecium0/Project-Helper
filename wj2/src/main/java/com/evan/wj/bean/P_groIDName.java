package com.evan.wj.bean;

public class P_groIDName {
    int gro_id;
    String gro_name;

    public P_groIDName(int gro_id, String gro_name) {
        this.gro_id = gro_id;
        this.gro_name = gro_name;
    }

    public int getGro_id() {
        return gro_id;
    }

    public void setGro_id(int gro_id) {
        this.gro_id = gro_id;
    }

    public String getGro_name() {
        return gro_name;
    }

    public void setGro_name(String gro_name) {
        this.gro_name = gro_name;
    }
}
